#pragma once

// Перечисление возможных ответов/действий со стороны игрока или интерфейса.
enum class Response
{
    OK,     // обычное продолжение игры, ход сделан успешно
    BACK,   // перейти назад (например, вернуться из игры в меню)
    REPLAY, // начать партию заново
    QUIT,   // полный выход из игры
    CELL    // клик по клетке игрового поля
};
